package MediaAcoes.src;
public class Semanas {
    
    float domingo, segunda, terça, quarta, quinta, sexta, sabado, mediaSemanal;

    Semanas(float domingo, float segunda, float terça, float quarta, float quinta, float sexta, float sabado, float mediaSemanal){
        this.domingo = domingo;
        this.segunda = segunda;
        this.terça = terça;
        this.quarta = quarta;
        this.quinta = quinta;
        this.sexta = sexta;
        this.sabado = sabado;
        this.mediaSemanal = mediaSemanal;
    }
}